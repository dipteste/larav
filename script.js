// Funções JavaScript
function showConfirmationPopup() {
    var popup = document.getElementById('confirmation-popup');
    popup.style.display = 'block';
}

function closeConfirmationPopup() {
    var popup = document.getElementById('confirmation-popup');
    popup.style.display = 'none';
}

